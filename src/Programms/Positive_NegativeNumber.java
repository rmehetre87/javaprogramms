package Programms;

public class Positive_NegativeNumber {

	public static void main(String[] args) {

		double Num = 0;
		
		if(Num > 0.0) {
			
			System.out.println(Num + " is positive number");
		}
		else if(Num < 0.0)
		{
			System.out.println(Num + " is Negative Number");
		}
		else
		{
			System.out.println(Num + " is zero");
		}
		
		
	}

}
